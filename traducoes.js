let idiomaAtual = localStorage.getItem("idioma") || "pt";


function traduzirPagina() {

    const idioma = traducoes[idiomaAtual];

    if (!idioma) return;

    document.querySelectorAll("[data-i18n]").forEach(elemento => {

        const chave = elemento.getAttribute("data-i18n");

        if (idioma[chave]) {
            elemento.textContent = idioma[chave];
        }

    });

}


function mudarIdioma(novoIdioma) {

    if (!traducoes[novoIdioma]) return;

    localStorage.setItem("idioma", novoIdioma);

    location.reload();

}


document.addEventListener("DOMContentLoaded", function() {

    traduzirPagina();

    const botaoIdioma = document.getElementById("botaoIdioma");

    const opcoesIdioma = document.getElementById("opcoesIdioma");

    if (botaoIdioma && opcoesIdioma) {

        botaoIdioma.addEventListener("click", function() {

            opcoesIdioma.classList.toggle("mostrar");

        });

    }

});